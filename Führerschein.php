                    <p>Als Pilotprojekt gestartet, ist der Führerschein mit 17 heute ein Erfolgsmodell.</p>
                    <h3 class="text-light">Warum sollte man schon mit 17 Jahren Auto fahren?</h3>
                    <p>Ziel des "Begleiteten Fahrens ab 17" ist es, die Verkehrssicherheit für junge Fahranfänger zu erhöhen. Junge Fahranfänger sind überdurchschnittlich oft Hauptverursacher von Unfällen mit Personenschäden. Die Gründe dafür sind mangelnde Erfahrung und eine erhöhte Risikobereitschaft. Nach bestandener Prüfung dürfen junge Fahranfänger ab 17 Jahren ein Fahrzeug führen, wobei jedoch bei jeder Fahrt eine Begleitperson anwesend sein muss. Diese Begleitperson(en) müssen in der Prüfbescheinigung eingetragen werden. Der Vorteil dieser Maßnahme ist folgender: Mäßiger Einfluss einer Begleitperson
                        Zusätzliche Fahrpraxis für den Fahranfänger - die Lernphase wird verlängert
                        Junge Fahrer können von der Erfahrung der Begleitpersonen profitieren.</p>
